function insert_prodotto($item_input) {
	$codice = HString::normalize($item_input['nome'], '_');
	$nome = $item_input['nome'];
	$keywords_ricerca = '';
	
	$find_list = [
		$codice,
		$nome,
		$keywords_ricerca,
	];
	$find = Utils::find_string($find_list);

	$userId = 1;
	
	$prezzo = $item_input['prezzo'];
	
	$dettaglio = [
		'patent' => '1',
		'lifetime' => '1',
		'universal_housing' => '1',
		'waterproof' => '1',	
	];
	$listino_cliente = [];
	$dettaglio['id_categoria_principale'] = $item_input['id_categoria_principale'];
	$dettaglio['id_categoria'] = $item_input['id_categoria'];
	$dettaglio['listino_cliente'] = [];
	
	$spese_spedizione = 'a';
	
	$params = [
		':ordine' => 0,
		':codice_old' => $item_input['codice_old'],
		':codice_iva' => $item_input['id_iva'],
		':esaurito' => 0,
		':abilitato' => 1,
		':spese_spedizione' => $spese_spedizione,
		':codice_fornitore' => '',
		':codice' => $codice,
		':prezzo' => $prezzo,
		':sconto' => 0,
		':id_azienda' => 0,
		':id_utente' => $userId,
		':dettaglio' => json_encode($dettaglio)
	];
		
	$strQry = ' ARTICOLO SET 
		CODICE_OLD = :codice_old, 
		CODICE_FORNITORE = :codice_fornitore, 
		CODICE = :codice, 
		ABILITATO = :abilitato, 
		ESAURITO = :esaurito,
		CODICE_IVA = :codice_iva, 
		ORDINE = :ordine,
		SPESE_SPEDIZIONE = :spese_spedizione, 
		ID_AZIENDA = :id_azienda, 
		PREZZO = :prezzo, 
		SCONTO = :sconto, 
		DATA_MODIFICA = now(),
		ID_UTENTE_MODIFICA = :id_utente,
		DETTAGLIO = :dettaglio';

	$nuovo = true;
	$strQry = 'INSERT INTO ' . $strQry . ', DATA_INSERIMENTO = now(), ID_UTENTE_INSERIMENTO = :id_utente_inserimento' ;
	$params[':id_utente_inserimento'] = $userId;

	$ps = H::db()->prepare($strQry);
	$ps->execute($params);
	$id = H::db()->lastInsertId();

	$list_lang = ['it', 'en', 'es'];
	foreach ($list_lang as $lang) {
		$dettaglio_lang = [
			'descrizione' => $item_input['descrizione_' . $lang]
		];
		Utils::update_dettaglio_lang('articolo', $id, $dettaglio_lang, $lang,
			[
				'find' => $find,
				'find_nome' => HString::normalize($nome),
				'nome' => $nome,
				'nome_simbolico' => '',				
				'documento_digitale_id' => 0,				
				'documento_digitale_nome' => '',				
			]);
	}
		
	H::call('manager/myarea/attributo/add', [
		'id_entita' => $id,
		'tipo' => 'categoria',
		'listId_attributo' => $item_input['id_categoria'],
		'list_attributo_valore' => '',
	]);
	
	$ps = H::db()->prepare('UPDATE ARTICOLO_ATTRIBUTO SET PRINCIPALE = false WHERE ID_ENTITA = :id_entita AND TIPO > 1 AND TIPO < 1000');
	$ps->execute([
		':id_entita' => $id,
	]);
	$ps = H::db()->prepare('UPDATE ARTICOLO_ATTRIBUTO SET PRINCIPALE = true WHERE ID_ENTITA = :id_entita AND
		ID_ATTRIBUTO = :id_attributo');
	$ps->execute([
		':id_attributo' => $dettaglio['id_categoria_principale'],
		':id_entita' => $id,
	]);
	
	$ps = H::db()->prepare('UPDATE ARTICOLO SET ID_VARIANTE = :id1 WHERE ID = :id2');
	$ps->execute([
		':id1' => $id,
		':id2' => $id,
	]);
	
	if (_GENERA_IMMAGINI) {
		$prima = true;
		foreach ($item_input['immagini'] as $image) {
			$php_info = pathinfo($image);
			$basename = HString::normalize($nome, '-') . '.' . $php_info['extension'];
			
			$image_id = AppUtils::storeImage($basename, 
								$image, 
								$id, 
								1,
								'image_dim',
								function($from, $to) {
									copy($from, $to);
								},
								_CREA_IMMAGINI);
			if ($prima) {
				$ps = H::db()->prepare('UPDATE IMMAGINE SET TIPO = 1 WHERE ID = :id AND TIPO_ENTITA = 1');
				$ps->bindValue(':id', $image_id);
				$ps->execute();
				$prima = false;
			}		
		}
		
		$principale = null;
		$secondaria = null;
		$list = [];
		$ps = H::db()->prepare('SELECT * FROM IMMAGINE WHERE ID_ENTITA = :id AND TIPO_ENTITA = 1');
		$ps->bindValue(':id', $id);
		$ps->execute();
		while ($row = $ps->fetch()) {
			$item = new Item($row);

			$item = [
				'src' => $item->id() . '.' . $item->get('estensione'),
				'alt' => $item->get('alt'),
				'name' => $item->get('name'),
			];

			switch ($row['TIPO']) {
			case 2:
				$secondaria = $item;
				break;
			case 1:
				$principale = $item;
				break;
			}
			$list[] = $item;
		}

		$ps = H::db()->prepare('SELECT * FROM ARTICOLO WHERE ID = :id');
		$ps->bindValue(':id', $id);
		$ps->execute();

		if ($row = $ps->fetch()) {
			$dettaglio = (array)json_decode($row['DETTAGLIO']);

			$dettaglio['immagini'] = [
				'principale' => $principale,
				'secondaria' => $secondaria,
				'list' => $list
			];
			
			$ps = H::db()->prepare('UPDATE ARTICOLO SET DETTAGLIO = :dettaglio WHERE ID = :id');
			$ps->bindValue(':dettaglio', json_encode($dettaglio));
			$ps->bindValue(':id', $id);
			$ps->execute();
		}
	}
	return $id;	
}


