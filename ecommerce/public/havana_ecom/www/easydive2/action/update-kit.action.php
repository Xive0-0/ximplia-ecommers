function insert_prodotto($item_input) {
	$codice = 'kit_' . $item_input['codice'];
	$ps = H::db()->prepare('SELECT * FROM ARTICOLO WHERE CODICE = :codice AND TIPO = 2');
	$ps->execute([
			':codice' => $codice,
		]);	
	if ($row = $ps->fetch()) {
		return $row['ID'];
	}
	
	$nome = $item_input['nome'];
	
	$find_list = [
		$codice,
		$nome,
	];
	$find = Utils::find_string($find_list);

	$userId = 1;
	
	$dettaglio = [];
	$listino_cliente = [];
	$dettaglio['id_obiettivo'] = $item_input['id_obiettivo'];
	$dettaglio['id_kit'] = $item_input['id_kit'];
	$dettaglio['listino_cliente'] = [];
	
	$params = [
		':ordine' => $item_input['ordine'],
		':codice_old' => '',
		':codice_iva' => 22,
		':esaurito' => 0,
		':abilitato' => 1,
		':spese_spedizione' => 'a',
		':codice_fornitore' => '',
		':codice' => $codice,
		':prezzo' => 0,
		':sconto' => 0,
		':id_azienda' => 0,
		':id_utente' => $userId,
		':dettaglio' => json_encode($dettaglio)
	];
		
	$strQry = ' ARTICOLO SET 
		TIPO = 2, 
		CODICE_OLD = :codice_old, 
		CODICE_FORNITORE = :codice_fornitore, 
		CODICE = :codice, 
		ABILITATO = :abilitato, 
		ESAURITO = :esaurito,
		CODICE_IVA = :codice_iva, 
		ORDINE = :ordine,
		SPESE_SPEDIZIONE = :spese_spedizione, 
		ID_AZIENDA = :id_azienda, 
		PREZZO = :prezzo, 
		SCONTO = :sconto, 
		DATA_MODIFICA = now(),
		ID_UTENTE_MODIFICA = :id_utente,
		DETTAGLIO = :dettaglio';

	$nuovo = true;
	$strQry = 'INSERT INTO ' . $strQry . ', DATA_INSERIMENTO = now(), ID_UTENTE_INSERIMENTO = :id_utente_inserimento' ;
	$params[':id_utente_inserimento'] = $userId;

	$ps = H::db()->prepare($strQry);
	$ps->execute($params);
	$id = H::db()->lastInsertId();

	$dettaglio_lang = [];
	$list_lang = ['it', 'en', 'es', 'fr', 'de'];
	foreach ($list_lang as $lang) {
		Utils::update_dettaglio_lang('articolo', $id, $dettaglio_lang, $lang,
			[
				'find' => $find,
				'find_nome' => HString::normalize($nome),
				'nome' => $nome,
				'nome_simbolico' => '',				
				'documento_digitale_id' => 0,				
				'documento_digitale_nome' => '',				
			]);
	}
	
	H::call('manager/myarea/attributo/add', [
		'id_entita' => $id,
		'tipo' => 'obiettivo',
		'listId_attributo' => $item_input['id_obiettivo'],
		'list_attributo_valore' => '',
	]);
	H::call('manager/myarea/attributo/add', [
		'id_entita' => $id,
		'tipo' => 'kit',
		'listId_attributo' => $item_input['id_kit'],
		'list_attributo_valore' => '',
	]);
		
	$ps = H::db()->prepare('UPDATE ARTICOLO SET ID_VARIANTE = :id1 WHERE ID = :id2');
	$ps->execute([
		':id1' => $id,
		':id2' => $id,
	]);

	return $id;	
}

function set_item_relazione($id, $list) {
	$tipo = 100;
	$ps = H::db()->prepare('DELETE FROM ARTICOLO_RELAZIONE WHERE (ID_ARTICOLO_1 = :id1 OR ID_ARTICOLO_2 = :id2) AND TIPO = :tipo');
	$ps->bindValue(':id1', $id);
	$ps->bindValue(':id2', $id);
	$ps->bindValue(':tipo', $tipo);
	$ps->execute();
	
	if ($list != null && count($list) > 0) {
		$ps = H::db()->prepare('INSERT INTO ARTICOLO_RELAZIONE SET 
			ID_ARTICOLO_1 = :id1, 
			ID_ARTICOLO_2 = :id2,
			ORDINE = 0,
			TIPO = :tipo');
		$params = [
			':id1' => $id,
			':tipo' => $tipo,
		];
		foreach ($list as $id2) {
			$params[':id2'] = $id2;
			$ps->execute($params);
		}			
	}
}

